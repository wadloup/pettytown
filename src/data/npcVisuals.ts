export const getNpcVisualProfile = (index: number, traits: PersonalityTrait[] = []): NPCVisual => {
  const base = npcVisualProfiles[index % npcVisualProfiles.length];
  const traitTitle = traits.map((trait) => titleByTrait[trait]).find(Boolean);

  return {
    ...base,
    title: traitTitle ?? base.title,
  };
};
